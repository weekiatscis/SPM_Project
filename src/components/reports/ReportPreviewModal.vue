<script setup>
import { ref, computed, watch, h, nextTick } from 'vue'
import {
  FileTextOutlined,
  CloseOutlined,
  DownloadOutlined,
  PieChartOutlined
} from '@ant-design/icons-vue'

const props = defineProps({
  isOpen: {
    type: Boolean,
    required: true
  },
  reportData: {
    type: Object,
    default: null
  }
})

const emit = defineEmits(['close', 'export'])

const modalVisible = ref(false)
const isExporting = ref(false)
const pieChartCanvas = ref(null)

// Watch for prop changes
watch(() => props.isOpen, (newVal) => {
  modalVisible.value = newVal
}, { immediate: true })

// Watch for reportData changes to draw pie chart
watch(() => props.reportData, (newVal) => {
  if (newVal) {
    nextTick(() => {
      drawPieChart()
    })
  }
}, { immediate: true })

const drawPieChart = () => {
  if (!pieChartCanvas.value || !props.reportData?.charts?.length) return

  const canvas = pieChartCanvas.value
  const ctx = canvas.getContext('2d')
  
  // Find the first pie chart
  const pieChart = props.reportData.charts.find(chart => chart.type === 'pie')
  if (!pieChart || !pieChart.data) return

  // Clear canvas
  ctx.clearRect(0, 0, canvas.width, canvas.height)

  // Prepare data
  const data = []
  const labels = []
  const colors = ['#22c55e', '#3b82f6', '#ef4444', '#a855f7', '#f59e0b', '#06b6d4']

  Object.entries(pieChart.data).forEach(([key, value]) => {
    if (value > 0) {
      data.push(value)
      labels.push(key)
    }
  })

  if (data.length === 0) {
    data.push(1)
    labels.push('No Data')
  }

  // Calculate total
  const total = data.reduce((a, b) => a + b, 0)

  // Draw pie chart
  const centerX = canvas.width / 2
  const centerY = 150
  const radius = 120

  let startAngle = -Math.PI / 2

  data.forEach((value, index) => {
    const sliceAngle = (value / total) * 2 * Math.PI
    const color = colors[index % colors.length]

    // Draw slice
    ctx.beginPath()
    ctx.moveTo(centerX, centerY)
    ctx.arc(centerX, centerY, radius, startAngle, startAngle + sliceAngle)
    ctx.closePath()
    ctx.fillStyle = color
    ctx.fill()
    ctx.strokeStyle = '#ffffff'
    ctx.lineWidth = 3
    ctx.stroke()

    startAngle += sliceAngle
  })

  // Draw legend
  const legendStartY = 320
  let legendY = legendStartY
  const legendX = 30

  ctx.font = '14px Arial'
  ctx.textAlign = 'left'

  labels.forEach((label, index) => {
    const color = colors[index % colors.length]
    
    // Draw color box
    ctx.fillStyle = color
    ctx.fillRect(legendX, legendY - 12, 16, 16)

    // Draw text
    ctx.fillStyle = '#1f2937'
    ctx.fillText(`${label}: ${data[index]}`, legendX + 24, legendY)

    legendY += 28
  })
}

