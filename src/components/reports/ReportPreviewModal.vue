    title: 'Task Title',
    dataIndex: 'title',
    key: 'title',
    width: '40%'
  },
  {
    title: 'Status',
    dataIndex: 'status',
    key: 'status',
    align: 'center'
  },
  {
    title: 'Priority',
    dataIndex: 'priority',
    key: 'priority',
    align: 'center'
  },
  {
    title: 'Due Date',
    key: 'due_date',
    align: 'center',
    customRender: ({ record }) => {
      const date = record.due_date
      if (!date) return 'N/A'
      try {
        return new Date(date).toLocaleDateString()
      } catch {
        return 'N/A'
      }
    }
  }
]

// Computed properties
const getReportTitle = () => {
  if (!props.reportData) return 'Report'
  
  const typeLabels = {
    'individual': 'Individual Report',
    'team': 'Team Report', 
    'department': 'Department Report',
    'organization': 'Organization Report'
  }
  
  return typeLabels[props.reportData.report_type] || 'Report'
}

const getTargetLabel = () => {
  if (!props.reportData) return ''
  
  const type = props.reportData.report_type
  if (type === 'individual') return 'Target User'
  if (type === 'team') return 'Team'
  if (type === 'department') return 'Department'
  if (type === 'organization') return 'Scope'
  return 'Target'
}

const getTargetInfo = () => {
  if (!props.reportData) return ''
  
  const summary = props.reportData.summary
  if (summary?.target_user) return summary.target_user
  if (summary?.team_name) return summary.team_name
  if (summary?.department) return summary.department
  if (summary?.scope_type) return `${summary.scope_type} analysis`
  return ''
}

const getFiltersInfo = () => {
  if (!props.reportData?.filters) return ''
  
  const filters = props.reportData.filters
  const parts = []
  
  if (filters.start_date && filters.end_date) {
    parts.push(`Date: ${filters.start_date} to ${filters.end_date}`)
  } else if (filters.start_date) {
    parts.push(`From: ${filters.start_date}`)
  } else if (filters.end_date) {
    parts.push(`Until: ${filters.end_date}`)
  }
  
  if (filters.status_filter && filters.status_filter.length > 0 && !filters.status_filter.includes('All')) {
    parts.push(`Status: ${filters.status_filter.join(', ')}`)
  }
  
  return parts.join(' | ') || 'No filters applied'
}

const getComparisonTitle = () => {
  if (!props.reportData) return 'Comparison'
  
  const type = props.reportData.report_type
  if (type === 'team') return 'Team Comparison'
  if (type === 'department') return 'Department Comparison'
  if (type === 'organization') return 'Organization Comparison'
  return 'Comparison'
}

const getComparisonData = () => {
  if (!props.reportData?.detailed_data) return []
  
  return props.reportData.detailed_data.team_comparison || 
         props.reportData.detailed_data.department_comparison || 
         props.reportData.detailed_data.scope_comparison || 
         []
}

const formatDate = (dateString) => {
  if (!dateString) return 'N/A'
  try {
    return new Date(dateString).toLocaleString()
  } catch {
    return 'N/A'
  }
}

const handleClose = () => {
  modalVisible.value = false
  emit('close')
}

const handleExportPDF = () => {
  emit('export')
}
</script>

